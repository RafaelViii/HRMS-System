/**
 * HTML Processor — post-processes captured HTML for static site use.
 *
 * Uses Cheerio to:
 *   1. Extract and localize Tailwind generated CSS
 *   2. Rewrite all URLs (links, assets, images) to local relative paths
 *   3. Disable forms
 *   4. Remove server-only scripts (keepalive, session meta, CSRF)
 *   5. Inject demo banner
 *   6. Convert SPA links to regular links
 */

const cheerio = require('cheerio');
const fs = require('fs');
const path = require('path');

class HTMLProcessor {
  /**
   * @param {import('./url-mapper')} urlMapper
   * @param {import('./asset-bundler')} assetBundler
   * @param {string} outputDir
   */
  constructor(urlMapper, assetBundler, outputDir) {
    this.urlMapper = urlMapper;
    this.assetBundler = assetBundler;
    this.outputDir = outputDir;
    this.tailwindCSSExtracted = false;
  }

  /**
   * Process raw HTML and return the static-site version.
   * @param {string} rawHTML — full HTML from page.evaluate
   * @param {string} htmlFilePath — target file path (e.g., pages/modules/employees/index.html)
   * @param {object} route — route definition
   * @returns {string} processed HTML
   */
  process(rawHTML, htmlFilePath, route) {
    const $ = cheerio.load(rawHTML, {
      decodeEntities: false,
      xmlMode: false,
    });

    // 1. Extract Tailwind generated CSS (only once)
    this._extractTailwindCSS($);

    // 2. Rewrite <head> — replace CDN references with local assets
    this._rewriteHead($, htmlFilePath);

    // 3. Rewrite all internal links
    this._rewriteLinks($, htmlFilePath);

    // 4. Rewrite image sources
    this._rewriteImages($, htmlFilePath);

    // 5. Disable forms
    this._disableForms($);

    // 6. Remove server-only elements
    this._removeServerScripts($);

    // 7. Convert SPA links to regular links
    this._convertSPALinks($);

    // 8. Inject demo banner script
    this._injectDemoBanner($, htmlFilePath);

    // 9. Update page title
    const title = route.label || 'HRIS Demo';
    $('title').text(`${title} — HRIS Demo`);

    // 10. Add meta tags for static site
    if (!$('meta[charset]').length) {
      $('head').prepend('<meta charset="UTF-8">');
    }

    return $.html();
  }

  _extractTailwindCSS($) {
    if (this.tailwindCSSExtracted) return;

    // Find the <style> tag generated by Tailwind CDN JIT
    // It's typically the largest inline style tag, or contains tailwind-specific classes
    $('style').each((i, el) => {
      const content = $(el).html();
      if (!content) return;

      // Tailwind generated CSS is usually very large (>10KB) and contains utility classes
      if (content.length > 5000 && (content.includes('.bg-') || content.includes('.flex') || content.includes('.text-'))) {
        const cssPath = path.join(this.outputDir, 'assets/css/tailwind-generated.css');
        fs.writeFileSync(cssPath, content);
        this.tailwindCSSExtracted = true;
      }
    });
  }

  _rewriteHead($, currentPage) {
    // Remove Tailwind CDN script tag
    $('script[src*="cdn.tailwindcss.com"]').remove();
    $('script[src*="tailwindcss"]').remove();

    // Remove Tailwind inline config script (the one with tailwind.config = {...})
    $('script').each((i, el) => {
      const content = $(el).html();
      if (content && content.includes('tailwind.config')) {
        $(el).remove();
      }
    });

    // Remove inline Tailwind-generated <style> tags (we extracted them to a file)
    if (this.tailwindCSSExtracted) {
      $('style').each((i, el) => {
        const content = $(el).html();
        if (content && content.length > 5000 && (content.includes('.bg-') || content.includes('.flex'))) {
          $(el).remove();
        }
      });
    }

    // Remove Google Fonts link (we have local copy)
    $('link[href*="fonts.googleapis.com"]').remove();

    // Build new <head> CSS references (relative to current page)
    const cssRefs = [
      'assets/css/inter-font.css',
      'assets/css/tailwind-generated.css',
      'assets/css/app.css',
    ];

    for (const cssRef of cssRefs) {
      const relPath = this.urlMapper.relativePath(currentPage, cssRef);
      const exists = $(`link[href="${relPath}"]`).length > 0;
      if (!exists) {
        // Insert before first existing <link> or at end of <head>
        const linkTag = `<link rel="stylesheet" href="${relPath}">`;
        const firstLink = $('head link[rel="stylesheet"]').first();
        if (firstLink.length) {
          firstLink.before(linkTag);
        } else {
          $('head').append(linkTag);
        }
      }
    }

    // Rewrite existing app.css link
    $('link[href*="app.css"]').each((i, el) => {
      const relPath = this.urlMapper.relativePath(currentPage, 'assets/css/app.css');
      $(el).attr('href', relPath);
    });

    // Rewrite Chart.js script
    $('script[src*="chart.js"], script[src*="chart.umd"]').each((i, el) => {
      const relPath = this.urlMapper.relativePath(currentPage, 'assets/js/chart.min.js');
      $(el).attr('src', relPath);
    });

    // Rewrite app.js script
    $('script[src*="app.js"]').each((i, el) => {
      const src = $(el).attr('src');
      // Skip if it's already our static version or demo-banner
      if (src && (src.includes('app-static') || src.includes('demo-banner'))) return;
      const relPath = this.urlMapper.relativePath(currentPage, 'assets/js/app-static.js');
      $(el).attr('src', relPath);
    });
  }

  _rewriteLinks($, currentPage) {
    $('a[href]').each((i, el) => {
      const $el = $(el);
      const href = $el.attr('href');
      if (!href) return;

      // Skip anchors, javascript:, mailto:, tel:, external
      if (href.startsWith('#') || href.startsWith('javascript:') ||
          href.startsWith('mailto:') || href.startsWith('tel:') ||
          href.startsWith('data:')) {
        return;
      }

      // Skip fully external URLs that aren't our base URL
      if ((href.startsWith('http://') || href.startsWith('https://')) &&
          !href.startsWith(this.urlMapper.baseUrl)) {
        return;
      }

      // Rewrite to local HTML file
      const newHref = this.urlMapper.rewriteURL(href, currentPage);
      $el.attr('href', newHref);
    });
  }

  _rewriteImages($, currentPage) {
    $('img[src]').each((i, el) => {
      const $el = $(el);
      const src = $el.attr('src');
      if (!src) return;

      // Skip data URIs and external images
      if (src.startsWith('data:') || (src.startsWith('http') && !src.startsWith(this.urlMapper.baseUrl))) {
        return;
      }

      // Map logo
      if (src.includes('logo')) {
        const relPath = this.urlMapper.relativePath(currentPage, 'assets/images/logo.jpg');
        $el.attr('src', relPath);
        return;
      }

      // For other app assets, try to rewrite path
      if (src.startsWith('/assets/') || src.startsWith(this.urlMapper.baseUrl + '/assets/')) {
        let assetPath = src.startsWith('/') ? src.slice(1) : src.replace(this.urlMapper.baseUrl + '/', '');
        const relPath = this.urlMapper.relativePath(currentPage, assetPath);
        $el.attr('src', relPath);
      }
    });
  }

  _disableForms($) {
    $('form').each((i, el) => {
      const $form = $(el);
      $form.removeAttr('action');
      $form.attr('onsubmit', 'event.preventDefault();return false;');
    });

    // Remove CSRF token inputs (they're server-side artifacts)
    $('input[name="csrf_token"], input[name="csrf"]').remove();
  }

  _removeServerScripts($) {
    $('script').each((i, el) => {
      const content = $(el).html();
      if (!content) return;

      // Remove keepalive configuration
      if (content.includes('__keepaliveCfg') || content.includes('keepalive')) {
        $(el).remove();
        return;
      }

      // Remove session metadata
      if (content.includes('__sessionMeta') || content.includes('sessionMeta')) {
        $(el).remove();
        return;
      }

      // Remove user access data exposure (window.__userAccess)
      if (content.includes('__userAccess')) {
        $(el).remove();
        return;
      }

      // Remove inline scripts that make server calls
      if (content.includes('/modules/auth/keepalive') || content.includes('api_')) {
        $(el).remove();
        return;
      }
    });

    // Remove logout links (they'd break in static mode)
    $('a[href*="logout"]').each((i, el) => {
      $(el).attr('href', '#');
      $(el).attr('onclick', 'alert("This is a demo — logout is disabled."); return false;');
    });
  }

  _convertSPALinks($) {
    // Remove 'spa' class from all links to disable SPA navigation
    $('a.spa').removeClass('spa');

    // Also remove any data-spa attributes
    $('a[data-spa]').removeAttr('data-spa');
  }

  _injectDemoBanner($, currentPage) {
    const relPath = this.urlMapper.relativePath(currentPage, 'assets/js/demo-banner.js');
    const scriptTag = `<script src="${relPath}" defer></script>`;

    // Insert before closing </body>
    $('body').append(scriptTag);
  }
}

module.exports = HTMLProcessor;
